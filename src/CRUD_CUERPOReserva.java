    public void ActualizarDatos(ObjectContainer base) {
        System.out.println("Código de Reserva: " + txtCodigoReserva.getText().trim());
        System.out.println("Código de Encabezado Reserva: " + cbCodigoReserva.getSelectedItem().toString());
        System.out.println("Código de Casa: " + cbCoidgoCasa.getSelectedItem().toString());

        // Verificar disponibilidad
        String disponibilidad = "";
        if (rbSi.isSelected()) {
            disponibilidad = "Si";
        } else if (rbNo.isSelected()) {
            disponibilidad = "No";
        }
        System.out.println("Disponibilidad: " + disponibilidad);

        if (txtCodigoReserva.getText().trim().isEmpty() || cbCodigoReserva.getSelectedItem() == null || cbCoidgoCasa.getSelectedItem() == null || (!rbSi.isSelected() && !rbNo.isSelected())) {
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos antes de ingresar", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                // Consultar la reserva existente en la base de datos
                Cuerpo_reserva reservacion = new Cuerpo_reserva(txtCodigoReserva.getText().trim(), null, null, null);
                ObjectSet<Cuerpo_reserva> res = base.get(reservacion);

                if (!res.isEmpty()) {
                    // Actualizar la reserva
                    Cuerpo_reserva reservacion1 = res.next();

                    // Additional check for required fields before updating
                    if (reservacion1.getCod_reserva() == null || reservacion1.getId_enc_reserva() == null || reservacion1.getCod_casa() == null || reservacion1.getDisponibilidad() == null) {
                        JOptionPane.showMessageDialog(this, "Por favor llene todos los campos antes de ingresar", "ERROR", JOptionPane.ERROR_MESSAGE);
                        return; // Exit the method if any required field is empty
                    }

                    reservacion1.setCod_reserva(txtCodigoReserva.getText().trim());
                    reservacion1.setId_enc_reserva(cbCodigoReserva.getSelectedItem().toString());
                    reservacion1.setCod_casa(cbCoidgoCasa.getSelectedItem().toString());
                    // Actualizar disponibilidad si es relevante
                    // reservacion1.setDisponibilidad(disponibilidad);
                    base.set(reservacion1);

                    // Verificar si se realizaron cambios antes de mostrar el mensaje de éxito
                    if (!reservacion.equals(reservacion1)) {
                        JOptionPane.showMessageDialog(this, "Modificación exitosa");
                    } else {
                        JOptionPane.showMessageDialog(this, "No se realizaron cambios", "Información", JOptionPane.INFORMATION_MESSAGE);
                    }

                    // Limpiar los campos después de la modificación
                    txtCodigoReserva.setText("");
                    cbCodigoReserva.setSelectedIndex(-1);
                    cbCoidgoCasa.setSelectedIndex(-1);
                    rbSi.setSelected(false);
                    rbNo.setSelected(false);

                    habilitarDatos();
                    cargarTabla(base);
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró la reserva con el código proporcionado", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al intentar actualizar la reserva", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

