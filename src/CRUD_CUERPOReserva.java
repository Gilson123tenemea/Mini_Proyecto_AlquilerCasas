    public void crearReservacion(ObjectContainer base) {
        try {
            // Validación de campos
            if (txtCodigoReserva.getText().trim().isEmpty() || cbCodigoReserva.getSelectedItem().toString() == null || cbCoidgoCasa.getSelectedItem().toString() == null || (!rbSi.isSelected() && !rbNo.isSelected())) {
                JOptionPane.showMessageDialog(this, "Por favor llene todos los campos antes de crear la reserva", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Determinar disponibilidad
            String disponibilidad = "";
            if (rbSi.isSelected()) {
                disponibilidad = "Si";
            } else if (rbNo.isSelected()) {
                disponibilidad = "No";
            }

            // Verificar si ya existe una reserva con el mismo código
            ObjectSet<Cuerpo_reserva> resul = base.queryByExample(new Cuerpo_reserva(txtCodigoReserva.getText().trim(), null, null, null));
            if (!resul.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ya existe un encabezado de reserva creado con este código", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Crear la reserva
            Cuerpo_reserva reservacion1 = new Cuerpo_reserva(txtCodigoReserva.getText().trim(), cbCodigoReserva.getSelectedItem().toString(), cbCoidgoCasa.getSelectedItem().toString(), disponibilidad);
            base.store(reservacion1);

            // Mostrar mensaje de éxito
            JOptionPane.showMessageDialog(this, "Reserva creada exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);

            // Limpiar campos
            limpiar();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al intentar crear la reserva", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            base.close();
        }
    }

