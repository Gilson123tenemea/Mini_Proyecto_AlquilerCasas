private void cargarTabla(ObjectContainer base) {
        DefaultTableModel model = (DefaultTableModel) jTableDatos.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de cargar los datos

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        ObjectSet<Cuerpo_reserva> result = base.queryByExample(Cuerpo_reserva.class);

        while (result.hasNext()) {
            Cuerpo_reserva reservacion = result.next();

            String disponibilidad = reservacion.getDisponibilidad();  // Valor predeterminado

            if (rbSi.isSelected()) {
                disponibilidad = "Si";
            } else if (rbNo.isSelected()) {
                disponibilidad = "No";
            }

            Object[] row = {
                reservacion.getId_enc_reserva(),
                reservacion.getId_enc_reserva(),
                reservacion.getCod_casa(),
                disponibilidad
            };

            model.addRow(row);
        }
    }
