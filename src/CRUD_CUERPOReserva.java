    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigoBuscar = JOptionPane.showInputDialog("Ingrese el código a buscar, por favor");
        ObjectContainer base = Db4o.openFile(INICIO.direccion);

        Query query = base.query();
        query.constrain(Cuerpo_reserva.class);
        query.descend("cod_reserva").constrain(codigoBuscar);

        ObjectSet<Cuerpo_reserva> result = query.execute();

        String[] columnNames = {"Código Reserva", "Código Encabezado Reserva", "Codigo Casa", "Disponibilidad"};

        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        int i = 0;
        for (Cuerpo_reserva reservacion : result) {
            Object[] row = {
                reservacion.getCod_reserva(),
                reservacion.getId_enc_reserva(),
                reservacion.getCod_casa(),
                reservacion.getDisponibilidad(),};
            model.addRow(row);
            i++;
        }

        if (i > 0) {
            JOptionPane.showMessageDialog(null, "Código encontrado");
            jTableDatos.setModel(model);
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el código");
        }

        base.close();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBUSCARCODIGORSERVAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBUSCARCODIGORSERVAActionPerformed
        // TODO add your handling code here:
        ObjectContainer base = Db4o.openFile(INICIO.direccion);

        String codigoReserva = "", codigoEncReserva = "", codigoCasa = "", disponibilidad = "";

        Query query = base.query();
        query.constrain(Cuerpo_reserva.class);
        query.descend("cod_reserva").constrain(txtCodigoReserva.getText().trim());
        ObjectSet<Cuerpo_reserva> result = query.execute();

        if (!result.isEmpty()) {
            habilitarDatos();  // Asumo que este método habilita los campos de texto u otros controles.
            for (Cuerpo_reserva reser : result) {
                codigoReserva = reser.getCod_reserva();
                codigoEncReserva = reser.getId_enc_reserva();
                codigoCasa = reser.getCod_casa();
                disponibilidad = reser.getDisponibilidad();
            }

        } else {
            JOptionPane.showMessageDialog(null, "No se encontró ninguna Reservación con el código ingresado");
        }
        inhabilitarDatosCed();
        base.close();
    }//GEN-LAST:event_btnBUSCARCODIGORSERVAActionPerformed

    private void btnReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        ObjectContainer base = Db4o.openFile(INICIO.direccion);

        ObjectSet<Cuerpo_reserva> result = base.queryByExample(new Cuerpo_reserva());
        String[] columnNames = {"Código Reserva", "Código Encabezado Reserva", "Codigo Casa", "Disponibilidad"};

        Object[][] data = new Object[result.size()][4];

        int i = 0;
        for (Cuerpo_reserva reser : result) {
            data[i][0] = reser.getCod_reserva();
            data[i][1] = reser.getId_enc_reserva();
            data[i][2] = reser.getCod_casa();
            data[i][3] = reser.getDisponibilidad();

            i++;
        }

        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        jTableDatos.setModel(model);
        base.close();
    }//GEN-LAST:event_btnReporteActionPerformed

