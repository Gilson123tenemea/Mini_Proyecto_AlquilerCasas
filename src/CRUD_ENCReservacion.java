    public void ActualizarDatos(ObjectContainer base) {
        // Verificar si todos los campos están llenos
        System.out.println("Código de Reserva: " + txtCodigoReserva.getText().trim());
        System.out.println("Código de Cliente: " + cbxCodigoCliente.getSelectedItem().toString());
        System.out.println("Fecha: " + jDateFecha.getDate());
        if (txtCodigoReserva.getText().trim().isEmpty() || cbxCodigoCliente.getSelectedItem().toString().isEmpty()
                || jDateFecha.getDate() == null) {

            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos antes de ingresar", "ERROR", JOptionPane.ERROR_MESSAGE);

        } else {
            try {
                // Consultar la reserva existente en la base de datos
                Enc_Reservacion reservacion = new Enc_Reservacion(txtCodigoReserva.getText().trim(), null, null);
                ObjectSet<Enc_Reservacion> res = base.get(reservacion);

                if (!res.isEmpty()) {
                    // Actualizar la reserva
                    Enc_Reservacion reservacion1 = res.next();
                    reservacion1.setId_enc_reserva(txtCodigoReserva.getText().trim());
                    reservacion1.setCod_cliente(cbxCodigoCliente.getSelectedItem().toString());
                    reservacion1.setFecha_reserva(jDateFecha.getDate());

                    base.set(reservacion1);

                    JOptionPane.showMessageDialog(this, "Modificación exitosa");

                    // Limpiar los campos después de la modificación
                    txtCodigoReserva.setText("");
                    cbxCodigoCliente.setSelectedItem("");
                    jDateFecha.setDate(null);
                    HabilitarDatos();
                    cargarTabla(base);
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró la reserva con el código proporcionado", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al intentar actualizar la reserva", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

