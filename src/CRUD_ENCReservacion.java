 private void cargarTabla(ObjectContainer base) {
        DefaultTableModel model = (DefaultTableModel) jTableDatos.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de cargar los datos

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        ObjectSet<Enc_Reservacion> result = base.queryByExample(Enc_Reservacion.class);

        while (result.hasNext()) {
            Enc_Reservacion reservacion = result.next();

            Object[] row = {
                reservacion.getId_enc_reserva(),
                reservacion.getCod_cliente(),
                reservacion.getFecha_reserva() != null ? sdf.format(reservacion.getFecha_reserva()) : null
            };

            model.addRow(row);
        }
    }

