    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String codigoEliminar = JOptionPane.showInputDialog("Ingrese el código a eliminar");
        boolean encontrado = false;
        ObjectContainer base = Db4o.openFile(INICIO.direccion);

        Query query = base.query();
        query.constrain(Enc_Reservacion.class);
        query.descend("id_enc_reserva").constrain(codigoEliminar);

        ObjectSet<Enc_Reservacion> result = query.execute();
        cargarTabla(base);
        if (result.size() > 0) {

            encontrado = true;

            int resul = JOptionPane.showConfirmDialog(null, "Deseas eliminar los datos de La Reserva", "Confirmacion", JOptionPane.YES_NO_OPTION);

            if (resul == JOptionPane.YES_OPTION) {
                for (Enc_Reservacion reservacion : result) {
                    // Eliminar el Cliente de la base de datos db4o
                    base.delete(reservacion);
                    JOptionPane.showMessageDialog(null, "Se están borrando los datos de la Reserva");
                    cargarTabla(base);
                }
            } else if (resul == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(null, "Datos de la Reserva no eliminados");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el código");
            cargarTabla(base);
        }

        base.close();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigoBuscar = JOptionPane.showInputDialog("Ingrese el código a buscar, por favor");
        ObjectContainer base = Db4o.openFile(INICIO.direccion);

        Query query = base.query();
        query.constrain(Enc_Reservacion.class);
        query.descend("id_enc_reserva").constrain(codigoBuscar);

        ObjectSet<Enc_Reservacion> result = query.execute();

        String[] columnNames = {"Código Reserva", "Código Cliente", "Fecha"};

        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        int i = 0;
        for (Enc_Reservacion reservacion : result) {
            Object[] row = {
                reservacion.getId_enc_reserva(),
                reservacion.getCod_cliente(),
                reservacion.getFecha_reserva()
            };
            model.addRow(row);
            i++;
        }

        if (i > 0) {
            JOptionPane.showMessageDialog(null, "Código encontrado");
            jTableDatos.setModel(model);
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el código");
        }

        base.close();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteActionPerformed
        // TODO add your handling code here:
        ObjectContainer base = Db4o.openFile(INICIO.direccion);

        ObjectSet<Enc_Reservacion> result = base.queryByExample(new Enc_Reservacion());
        String[] columnNames = {"Código Reserva", "Código Cliente", "Fecha"};

        Object[][] data = new Object[result.size()][3];

        int i = 0;
        for (Enc_Reservacion reser : result) {
            data[i][0] = reser.getId_enc_reserva();
            data[i][1] = reser.getCod_cliente();
            data[i][2] = reser.getFecha_reserva();

            i++;
        }

        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        jTableDatos.setModel(model);
        base.close();
    }//GEN-LAST:event_btnReporteActionPerformed

