    public void cargarTabla(ObjectContainer base) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de cargar los datos

        ObjectSet<Comentario> result = base.queryByExample(new Comentario());

        while (result.hasNext()) {
            Comentario personal1 = result.next();

            Object[] row = {
                personal1.getCod_comentario(),
                personal1.getCod_cliente(),
                personal1.getCod_casa(),
                personal1.getDescrpcion(),};
            model.addRow(row);
        }

    }
